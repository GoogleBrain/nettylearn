package com.sun.messagepackwithnetty;

import org.msgpack.annotation.Message;

@Message
public class UserInfo {
	private String name;
	private int id;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
}
